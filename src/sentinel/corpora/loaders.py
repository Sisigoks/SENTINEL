"""Adversarial probe loaders.

Probes are **never generated by SENTINEL** (Invariant I1). They are loaded read-only
from published red-team corpora and standard benchmark suites. Every probe carries a
SHA-256 provenance hash and a registered source; a probe without a source is rejected
(Invariant I5).

The primary path is :func:`load_jsonl_corpus`, which reads externally provided corpora
(e.g. exports of public datasets mapped to the OWASP classes). A small *illustrative*
built-in seed set is provided only so the pipeline and tests are runnable out-of-the-box;
it consists of canonical, widely-published example patterns (Perez & Ribeiro 2022;
Greshake et al. 2023) and is clearly labeled as illustrative, not a research corpus.
"""

from __future__ import annotations

import hashlib
import json
from dataclasses import dataclass, field
from pathlib import Path

from ..core.types import InputChannel, ThreatClass

REGISTERED_SOURCES: set[str] = {
    "owasp-llm-top10-2025",
    "owasp-asi-top10-2026",
    "mitre-atlas",
    "greshake-2023-indirect-injection",
    "perez-ribeiro-2022-ignore-prompt",
    "builtin-illustrative",  # the small seed set below
}


@dataclass(slots=True)
class Probe:
    text: str
    threat_class: ThreatClass
    channel: InputChannel
    source: str
    held_out: bool = False
    canary: str | None = None  # token whose appearance in output proves leakage
    requested_scope: set[str] = field(default_factory=set)
    granted_scope: set[str] = field(default_factory=set)

    @property
    def provenance_hash(self) -> str:
        return hashlib.sha256(f"{self.source}|{self.text}".encode()).hexdigest()


@dataclass
class ProbeCorpus:
    probes: list[Probe]
    name: str = "corpus"

    def __post_init__(self) -> None:
        for p in self.probes:
            if p.source not in REGISTERED_SOURCES:
                raise ValueError(f"probe from unregistered source {p.source!r} (Invariant I5)")

    def by_class(self, c: ThreatClass) -> list[Probe]:
        return [p for p in self.probes if p.threat_class is c]

    def seen(self) -> list[Probe]:
        return [p for p in self.probes if not p.held_out]

    def held_out(self) -> list[Probe]:
        return [p for p in self.probes if p.held_out]

    def manifest(self) -> dict:
        return {
            "name": self.name,
            "n": len(self.probes),
            "by_class": {c.value: len(self.by_class(c)) for c in ThreatClass},
            "hashes": sorted(p.provenance_hash for p in self.probes),
        }


def load_jsonl_corpus(path: str | Path, name: str = "external") -> ProbeCorpus:
    """Load probes from a JSONL file. Each line: {text, threat_class, channel, source, ...}."""
    probes: list[Probe] = []
    for line in Path(path).read_text(encoding="utf-8").splitlines():
        if not line.strip():
            continue
        d = json.loads(line)
        probes.append(
            Probe(
                text=d["text"],
                threat_class=ThreatClass(d["threat_class"]),
                channel=InputChannel(d.get("channel", "user_task")),
                source=d["source"],
                held_out=d.get("held_out", False),
                canary=d.get("canary"),
                requested_scope=set(d.get("requested_scope", [])),
                granted_scope=set(d.get("granted_scope", [])),
            )
        )
    return ProbeCorpus(probes=probes, name=name)


